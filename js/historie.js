$(function () {
   // Dějiny Argentiny
   articles.forEach((articleTitle) => {
      $("#articles .list-group").append(`<li class="list-group-item list-group-item-action list-group-item-dark">${articleTitle.title}</li>`);
   });

   function fillArticleCard(articleName) {
      let article = articles.find(item => { return item.title === articleName });
      $("#title").text(article.title);
      $("#text").text(article.text);
   }

   $("#articles li:first").addClass('active');
   fillArticleCard(articles[0].title);

   $("#articles li").on("click", function () {
      $("#articles li").removeClass("active");
      $(this).addClass("active");
      let article = $(this).text();
      $("#portret").fadeOut("fast", function () {
         fillArticleCard(article);
      });
      $("#portret").fadeIn("fast");
   });

   // Postavy
   postavy.forEach((postava) => {
      $("#postavy").append(`<div id="${postava.idImg}" style="background: url('photos/${postava.img}'); width: 300px;height: 385px;position: relative;background-repeat: no-repeat;margin:auto; ">
      <div id="${postava.idText}" style="position: absolute; width: 300px; height: 60%;bottom: 0;background-color: white; color: black;display: none;border-width: -5px;border-style:
      solid;"><div style="text-align: center" ><h3 >${postava.name}</h3> <i class="fas fa-star-of-life"></i> <b>${postava.birth}</b> - <i class="fas fa-cross"></i> <b>${postava.death}</b></div> <p style="margin-left:2%;">${postava.text}</p></div></div>`);
      $("#"+postava.idImg+"").hover(function () {
         $("#"+postava.idText+"").slideToggle('slow');
      });
   })

   // Památky
   pamatky.forEach((pamatka) => {
     $("#"+pamatka.id + "Title").text(pamatka.title);
     $("#"+pamatka.id + "Text").text(pamatka.text);
     $("#"+pamatka.id + "Img").attr("src", "photos/"+ pamatka.img);
     $("#"+pamatka.id + "Odkaz").attr("href", ""+pamatka.odkaz+"");
     $("#"+pamatka.id + "").hover(function () {
      $(this).animate({width: "+=8", height: "+=8" });
      },function(){
         $(this).animate({ width: "-=8", height: "-=8" });
      });
   })
})